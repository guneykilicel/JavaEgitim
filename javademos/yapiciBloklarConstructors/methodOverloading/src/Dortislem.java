public class Dortislem {
    public int topla(int sayi1, int sayi2){
        return sayi1+sayi2;
    }
// farkındaysan ikisi de topla ama biri 3 değer istiyor diğeri 2 kullanıcının verdiği değerlere göre işlem yapacak
    public int topla(int sayi1, int sayi2, int sayi3){
        return sayi1+sayi2+sayi3;
    }
}
